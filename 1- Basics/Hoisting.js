// Concepto de Hoisting
// Podemos usar una Variable, luego declararla
// Nota: esto no funciona como tal con la palabra reservada Let, solo funciona declarando con var

var x = 10;
console.log(x);


//Esto se puede dividir en dos partes, primero declarar y luego asignar valor, Esto se conoce como Hoisting
var y; // Declarar la variable 
y = 25; // Inicializar y dar valor