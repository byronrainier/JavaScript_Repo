// Tipos de datos en JS
let miEntero = 10;
console.log(miEntero);
console.log("La primera variable es de tipo: " + typeof miEntero);

let miFlotante = 7.9;
console.log(miFlotante);
console.log("La segunda variable es de tipo: " + typeof miFlotante);

let miCadena = "Hola";
console.log(miCadena);
console.log("La tercera variable es de tipo: " + typeof miCadena);

let miBoolean = true; // true o false
console.log(miBoolean);
console.log("La cuarta variable es de tipo: " + typeof miBoolean);

let miNull = null;
console.log(miNull);
console.log("La quinta variable es de tipo: " + typeof miNull);

let miUndefined = undefined;
console.log(miUndefined);
console.log("La sexta variable es de tipo: " + typeof miUndefined);