// Ciclo While
// Solo se ejecuta solo si la condicion inicial se cumple, luego de sigue validando mientras sea verdadera.
/**
 * Sintaxis
 * while (Condicion) valor booleano
 * {
 *  bloque de codigo a ejecutar
 * }
 * 
 */
let contador = 0;
let repeticiones = 5;

while (contador <= repeticiones){
    console.log(contador);
    contador++;
}